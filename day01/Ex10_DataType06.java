public class Ex10_DataType06 {
    public static void main(String[] args){
        /*
         * 실수
         * float
         * double
         */

        float f = 2.5f;
        System.out.println(f);

        double d = 2.5;
        System.out.println(d);

        // 지수 표기법(Scientific Notation)을 사용한 double 타입
        double d2 = 1.0e3;
        System.out.println(d2);
    }
}
